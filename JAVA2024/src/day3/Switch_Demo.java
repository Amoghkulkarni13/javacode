package day3;

public class Switch_Demo {
	
	
	public static void main(String args[]) {
		
			String week_day="Sunday";	
		switch(week_day) {
		
		case "Sunday": System.out.println(1); break;
		case "Monday": System.out.println(2); break;
		case "Tuesday": System.out.println(3); break;
		case "Wed": System.out.println(4); break;
		case "Thur": System.out.println(5); break;
		case "fri": System.out.println(6); break;
		case "sat": System.out.println(7); break;
		default: System.out.println("invalid week_no");

		}
		
	
		
		
		
		
	}

}
