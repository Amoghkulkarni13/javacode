package day4;

public class Do_While_Demo {

	public static void main(String args[]) {
		
		
		int i=1;
		
		do {
			
			System.out.println(i);
			i++;
		}while(i<=10);
		
		System.out.println("-------------------------------------");
		//print from 10 to 1
		
		
		int f=10;
		
		do {
			
			System.out.println(f);
			f--;
		}while(f>=1);
	}
}
